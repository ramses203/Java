package chapter04;

public class If_example03 {
	public static void main(String[] args) {

		int age = 5;

		if (age >= 8) { // if조건이 참이면 {수행문을 실행}

			System.out.println("학교에 다닙니다."); // 조건이 참일때 실행
		}

		else { // if조건이 참이 아니면 {수행문을 실행}
			System.out.println("학교에 다니지 않습니다."); // 조건이 거짓일떄 실행
		}

	}

}
